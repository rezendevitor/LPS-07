    public void update(){
        try {
            servico = new Object[6];
            SimpleDateFormat formatoData = new SimpleDateFormat("yyyy-MM-dd");
            Date data = formatoData.parse(ftxData.getText());
            java.sql.Date datasql = new java.sql.Date(data.getTime());  
            servico[0] = Integer.parseInt(edtId.getText());
            servico[1] = cbxTipo.getSelectedItem().toString();
            servico[2] = edtLocalID.getText();
            servico[3] = datasql;
            servico[4] = ftxCPF.getText();
            servico[5] = Float.parseFloat(edtValor.getText());
            servicoController.alterar(servico);
        } catch (ParseException ex) {
            Logger.getLogger(FrCadastroServico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(validarCampos()){
            try {
                if(this.flagEdtSalvar == true){
                    this.update();
                    this.atualizarLog("Servi√ßo " + edtId.getText() + " alterado.");
                }else{
                    servico = new Object[6];
                    SimpleDateFormat formatoData = new SimpleDateFormat("yyyy-MM-dd");
                    Date data = formatoData.parse(ftxData.getText());
                    java.sql.Date datasql = new java.sql.Date(data.getTime());  
                    servico[0] = Integer.parseInt(edtId.getText());
                    servico[1] = cbxTipo.getSelectedItem().toString();
                    servico[2] = edtLocalID.getText();
                    servico[3] = datasql;
                    servico[4] = ftxCPF.getText();
                    servico[5] = Float.parseFloat(edtValor.getText());
                    servicoController.inserir(servico);
                }
                this.atualizarTabela(null);
                this.habilitarCampos(false);
                this.habilitarBotoes(true, false, true, true, false, true, true, false);
                if(this.flagEdtSalvar == false){
                    this.atualizarLog("Servi√ßo " + edtId.getText() + " salvo.");
                }
                this.resetFields();
            } catch (ParseException ex) {
                Logger.getLogger(FrCadastroServico.class.getName()).log(Level.SEVERE, null, ex);
            }
        }       
    }//GEN-LAST:event_btnSalvarActionPerformed

