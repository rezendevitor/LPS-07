    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        unidCons = new Object[5];
        if(grdUsuarios.getSelectedRow() != -1){
            int id = (int) grdUsuarios.getValueAt(grdUsuarios.getSelectedRow(), 0);
            unidCons[0] = id;
            unidCons = unidConsController.buscarUnidCons(unidCons).get(0);
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione uma Unidade Consumidora (clique na linha).");
        }if(unidCons != null){
            edtId.setText(unidCons[0].toString());
            cbxTipo.setSelectedItem(unidCons[1]);
            edtEndereco.setText(unidCons[2].toString());
            ftxCPF.setText(unidCons[3].toString());
            edtConsumo.setText(unidCons[4].toString()); 
            this.habilitarBotoes(false, true, false, false, true, false, false, false);
            this.habilitarCampos(true);
            edtId.setEnabled(false);
            this.flagCancelar = true;
            this.flagEdtSalvar = true;
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int id = -1;
        unidCons = new Object[5];
        if(grdUsuarios.getSelectedRow() != -1){
            id = (int) grdUsuarios.getValueAt(grdUsuarios.getSelectedRow(), 0);
            unidCons[0] = id;
            unidCons = unidConsController.buscarUnidCons(unidCons).get(0);
        }if(unidCons != null){
            int op = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir?", "Escolha uma opção", JOptionPane.YES_NO_OPTION);
            if(op == JOptionPane.YES_OPTION){
                unidConsController.excluir(unidCons);
                this.habilitarBotoes(true, false, true, true, false, true, true, false);
                this.atualizarTabela(null);
                this.resetFields();
                this.habilitarCampos(false);
                this.atualizarLog("Usuário " + id + " excluído.");
            }  
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione um usuário.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int id = Integer.parseInt(JOptionPane.showInputDialog("Informe um ID: "));
        unidCons = new Object[5];
        unidCons[0] = id;
        unidConsController.buscarUnidCons(unidCons);
        if(id < 0){
            this.atualizarLog("Operação cancelada.");
        }else if(unidCons == null){
            JOptionPane.showMessageDialog(rootPane, "Unidade Consumidora não encontrada.");
        }else{
            this.habilitarBotoes(true, false, true, true, false, false, true, true);
            this.habilitarCampos(false);
            this.flagCancelar = true;
            this.flagEdtSalvar = true;
            this.atualizarLog("Busca concluída");
            this.atualizarTabela(unidCons);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

