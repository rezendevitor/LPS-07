    public void update(){
        unidCons = new Object[5];
        unidCons[0] = Integer.parseInt(edtId.getText());
        unidCons[1] = ftxCPF.getText();
        unidCons[2] = cbxTipo.getSelectedItem().toString();
        unidCons[3] = Float.parseFloat(edtConsumo.getText());
        unidCons[4] = edtEndereco.getText();
        unidConsController.alterar(unidCons);
    }
    
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(validarCampos()){
            if(this.flagEdtSalvar == true){
                this.update();
                this.atualizarLog("Unidade Consumidora " + edtId.getText() + " alterada.");
            }else{
                unidCons = new Object[5];
                unidCons[0] = Integer.parseInt(edtId.getText());
                unidCons[1] = ftxCPF.getText();
                unidCons[2] = cbxTipo.getSelectedItem().toString();
                unidCons[3] = Float.parseFloat(edtConsumo.getText());
                unidCons[4] = edtEndereco.getText();
                unidConsController.alterar(unidCons);
            }    
            this.atualizarTabela(null);
            this.habilitarCampos(false);
            this.habilitarBotoes(true, false, true, true, false, true, true, false);
            if(this.flagEdtSalvar == false){
                this.atualizarLog("Unidade Consumidora " + edtId.getText() + " salva.");
            }
            this.resetFields();
        }        
    }//GEN-LAST:event_btnSalvarActionPerformed

