    public void atualizarTabela(Object [] usr) {
        ArrayList<Object []> arrayObj = unidConsController.buscarUnidCons(usr);
        DefaultTableModel dtm = (DefaultTableModel) grdUsuarios.getModel();
        dtm.setNumRows(0);
        for (int i=0; i<arrayObj.size(); i++) {
            dtm.addRow(new Object[]{
                arrayObj.get(i)[0], arrayObj.get(i)[1], arrayObj.get(i)[2], arrayObj.get(i)[3], arrayObj.get(i)[4]});
        }
        grdUsuarios.setModel(dtm);
    }
    
