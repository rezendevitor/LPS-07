    public void formatarCampos(){
        try {
            this.edtId.setDocument(new PadraoNumeros());
            MaskFormatter maskCPF = new MaskFormatter("###.###.###-##");
            maskCPF.install(ftxCPF);
            this.edtConsumo.setDocument(new PadraoNumeros());
            this.txtLog.setEditable(false);
            this.atualizarLog("Programa iniciado.");
        } catch (ParseException ex) {
            Logger.getLogger(FrCadastroServico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public boolean validarCPF(String cpf){
        int digito1 = 0, digito2 = 0, calculoDigito1 = 0, calculoDigito2 = 0, j = 10, z = 11;
        boolean repetido = true;
        digito1 = Integer.parseInt(cpf.substring(12, 13));
        digito2 = Integer.parseInt(cpf.substring(13, 14));
        cpf = cpf.substring(0, 3) + cpf.substring(4, 7) + cpf.substring(8, 11);
        int[] arrayCPF = new int[9];
        for(int i = 0; i < arrayCPF.length; i++) {
            arrayCPF[i] = Integer.parseInt(cpf.substring(i, i+1));
            calculoDigito1 += j * arrayCPF[i];
            j--;
            calculoDigito2 += z * arrayCPF[i];
            z--;
            if (arrayCPF[0] != arrayCPF[1] && repetido) {
                repetido = false;
            }        
        }
        calculoDigito2 += z * digito1;
        calculoDigito1 = (calculoDigito1 * 10) % 11;
        calculoDigito2 = (calculoDigito2 * 10) % 11;
        
        if (calculoDigito1 == 10) {
            calculoDigito1 = 0;
        }
        if (calculoDigito2 == 10) {
            calculoDigito2 = 0;
        }
        if(calculoDigito1 != digito1 || calculoDigito2 != digito2 || repetido == true){
            return false;
        }else{
            return true;
        }
    }
    
    public boolean validarCampos(){
        if(edtId.getText().replace(" ","").equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digite o ID corretamente.");
            edtId.requestFocus();
            return false;
        }else{
            if(unidConsController.existe(edtId.getText()) == true && flagEdtSalvar == false){
                JOptionPane.showMessageDialog(rootPane, "ID já cadastrado.");
                edtId.setText("");
                edtId.requestFocus();
                return false;
            }
        }if(cbxTipo.getSelectedItem().toString().equals("<Selecione>")){
            JOptionPane.showMessageDialog(rootPane, "Por favor selecione um tipo.");
            cbxTipo.requestFocus();
            return false;
        }if(ftxCPF.getText().replace(" ","").length() < 14){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digite o CPF corretamente.");
            ftxCPF.requestFocus();
            return false;
        }else{
            if(!validarCPF(ftxCPF.getText())){
                JOptionPane.showMessageDialog(rootPane, "CPF Inválido!");
                ftxCPF.setText("");
                ftxCPF.requestFocus();
                return false;
            }
        }if(edtEndereco.getText().replace(" ","").equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digite o endereço corretamente.");
            edtEndereco.requestFocus();
            return false;
        }if(edtConsumo.getText().replace(" ","").equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor, digite o consumo corretamente.");
            edtConsumo.requestFocus();
            return false;
        }
        return true;
    }
    
